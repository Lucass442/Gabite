    <h1>Receita do Hambúrguer de Siri</h1>
        <p>O Hambúrguer de Siri é o famoso e secreto sanduíche do restaurante Siri Cascudo, na série Bob Esponja. Sua receita, guardada a sete chaves pelo Seu Siriguejo, nunca foi totalmente revelada.</p>
        <br>
    <h2>Ingredientes</h2>
        <ul><li>400g de carne de siri desfiada (pode ser fresca, congelada ou enlatada, desde que seja de boa qualidade)</li></ul>
        <ul><li>½ cebola picada</li></ul>
        <ul><li>2 dentes de alho picados</li></ul>
        <ul><li>1 ovo batido</li></ul>
        <ul><li>½ xícara (chá) de farinha de rosca</li></ul>
        <ul><li>2 colheres (sopa) de azeite</li></ul>
        <ul><li>Suco de ½ limão pequeno</li></ul>
        <ul><li>Sal e pimenta-do-reino a gosto</li></ul>
        <ul><li>Páprica defumada ou picante a gosto (opcional)</li></ul>
        <br>
    <h2>Para a montagem</h2>
        <ul><li>Pães de hambúrguer</li></ul>
        <ul><li>Queijo prato ou muçarela</li></ul>
        <ul><li>Alface americana</li></ul>
        <ul><li>Tomate em rodelas</li></ul>
        <ul><li>Picles em fatias</li></ul>
        <ul><li>Ketchup, mostarda e maionese a gosto</li></ul>
        <br>
    <h2>Modo de Preparo</h2>
        <h3>1. Prepare a massa do hambúrguer</h3>
        <ol><li>Em uma tigela, misture a carne de siri desfiada, a cebola e o alho picados.</li></ol>
        <ol><li>Adicione o ovo batido, o suco de limão, sal, pimenta-do-reino e a páprica. Misture tudo delicadamente com as mãos ou uma colher.</li></ol>
        <ol><li>Acrescente a farinha de rosca, aos poucos, até a massa ficar mais consistente e fácil de moldar. A farinha de rosca ajuda a dar liga e a evitar que o hambúrguer desfaça na frigideira.</li></ol>
        <br>
        <h3>2. Modele os hambúrgueres</h3>
        <ol><li>Divida a massa em porções e modele os hambúrgueres. Você pode usar um aro para deixar o formato mais certinho ou fazer com as mãos mesmo.</li></ol>
        <ol><li>Leve os hambúrgueres para a geladeira por uns 15 a 20 minutos. Isso ajuda a firmar a massa e evita que eles se quebrem durante o cozimento.</li></ol>
        <br>
        <h3>3. Cozinhe os hambúrgueres</h3>
        <ol><li>Em uma frigideira antiaderente, aqueça o azeite em fogo médio.</li></ol>
        <ol><li>Doure os hambúrgueres por cerca de 2 a 3 minutos de cada lado, até que fiquem bem firmes e com uma crosta dourada.</li></ol>
        <ol><li>Se for adicionar queijo, coloque-o sobre o hambúrguer nos últimos minutos de cozimento e abafe a frigideira para derreter.</li></ol>
        <br>
        <h3>4. Monte o seu lanche</h3>
        <ol><li>Corte os pães de hambúrguer e doure-os levemente na mesma frigideira ou em uma torradeira.</li></ol>
        <ol><li>Atenção! Esta é a ordem secreta do desenho: Pão, hambúrguer de siri, alface, queijo, cebola, tomate, ketchup, mostarda, picles e, por fim, a outra metade do pão.</li></ol>
        <ol><li>Sirva imediatamente e aproveite seu lanche</li></ol>
